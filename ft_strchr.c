#include "libft.h"

char	*ft_strchr(char *s, int c)
{
	int i;

	i = 0;
	if (c == '\0')
		return (s);
	while (*s != '\0')
	{
		if (*s == c)
			return (s);
		s++;
	}
	return (0);
}
