#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	while (*s)
		s++;
	if (c == 0)
		return (s);
	s--;
	if (*s == c)
		return (s);
	while (*s != 'c')
	{
		s--;
		if (*s == c)
			return (s);
	}
	return (NULL);
}
